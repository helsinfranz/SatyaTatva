export function getCategories() {
  return Object.keys(data)
    .map((category) => {
      return Object.keys(data[category]).map((sub) => {
        return { params: { category, sub } };
      });
    })
    .flat();
}

export function getCategoriesArray(category, sub) {
  if (!sub || !category) {
    return [];
  }
  if (data[category]) {
    if (data[category][sub]) {
      return data[category][sub];
    }
  }
  return [];
}

export function getSingleShlokData(slug) {
  if (slug) {
    const shloks = data["others"]["shloks"];
    const check = shloks.find((shlok) => shlok.slug === slug);
    return check;
  }
  return null;
}

